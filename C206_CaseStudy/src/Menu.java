public class Menu {
	private int month;
	private String name;
	
	public Menu(int month, String name) {
		this.month = month;
		this.name = name;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	} 
	
	
	
	
}//end of class
